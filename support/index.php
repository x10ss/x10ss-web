<h3>propriotery / open</h3>
	<p class="chop-book">
	Proprietary file extensions are owned or controlled by a specific company or software. They may only work fully with that software and aren’t always compatible with others.<br/>

	Examples: .docx (Microsoft Word), .psd (Adobe Photoshop), .xlsx (Excel)<br/>

	Open file extensions are publicly documented and can be used or implemented by anyone. They promote compatibility across different programs.<br/>

	Examples: .txt, .csv, .html, .png<br/>

	Key difference: Proprietary extensions are software-specific; open extensions are universal and widely supported.
